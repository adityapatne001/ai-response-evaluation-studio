def save_evaluation_history(
    prompt,
    response_1_score,
    response_2_score,
    response_3_score,
    best_response,
    weakest_response
):
    """
    Saves evaluation results into CSV history.
    """

    new_record = {
        "prompt": prompt,
        "response_1_score": response_1_score,
        "response_2_score": response_2_score,
        "response_3_score": response_3_score,
        "best_response": best_response,
        "weakest_response": weakest_response
    }

    df_new = pd.DataFrame([new_record])

    file_path = "data/evaluation_history.csv"

    df_existing = pd.read_csv(file_path)

    df_updated = pd.concat(
        [df_existing, df_new],
        ignore_index=True
    )

    df_updated.to_csv(file_path, index=False)


def display_evaluation_monitoring_panel():
    """
    Displays AI evaluation monitoring panel.
    """

    file_path = "data/evaluation_history.csv"

    df = pd.read_csv(file_path)

    # -----------------------------------
    # CHECK IF DATA EXISTS
    # -----------------------------------

    if df.empty:

        st.warning("No evaluation history found.")

        return

    # -----------------------------------
    # DISPLAY EVALUATION HISTORY
    # -----------------------------------

    st.subheader("Evaluation History Records")

    st.dataframe(df)

    # -----------------------------------
    # AVERAGE RESPONSE SCORES
    # -----------------------------------

    average_scores = {
        "Response 1": df["response_1_score"].mean(),
        "Response 2": df["response_2_score"].mean(),
        "Response 3": df["response_3_score"].mean()
    }

    # -----------------------------------
    # RESPONSE PERFORMANCE CHART
    # -----------------------------------

    st.subheader("Average Response Quality Scores")

    fig, ax = plt.subplots()

    ax.bar(
        average_scores.keys(),
        average_scores.values()
    )

    ax.set_ylabel("Average Score")

    ax.set_title("Average AI Response Evaluation Scores")

    st.pyplot(fig)

    # -----------------------------------
    # BEST RESPONSE DISTRIBUTION
    # -----------------------------------

    st.subheader("Best Performing Response Frequency")

    best_counts = df["best_response"].value_counts()

    fig2, ax2 = plt.subplots()

    ax2.bar(
        best_counts.index,
        best_counts.values
    )

    ax2.set_ylabel("Frequency")

    ax2.set_title("Most Frequently Best Performing Response")

    st.pyplot(fig2)


def generate_evaluation_report():
    """
    Generates evaluation summary report.
    """

    file_path = "data/evaluation_history.csv"

    df = pd.read_csv(file_path)

    # -----------------------------------
    # CHECK IF DATA EXISTS
    # -----------------------------------

    if df.empty:

        return "No evaluation records available."

    # -----------------------------------
    # CALCULATE SUMMARY STATISTICS
    # -----------------------------------

    total_evaluations = len(df)

    average_response_1 = round(
        df["response_1_score"].mean(),
        2
    )

    average_response_2 = round(
        df["response_2_score"].mean(),
        2
    )

    average_response_3 = round(
        df["response_3_score"].mean(),
        2
    )

    most_common_best = (
        df["best_response"]
        .value_counts()
        .idxmax()
    )

    # -----------------------------------
    # CREATE REPORT TEXT
    # -----------------------------------

    report = f"""
AI RESPONSE EVALUATION REPORT
====================================

Total Evaluations Conducted:
{total_evaluations}

Average Response Scores:
------------------------------------
Response 1: {average_response_1}
Response 2: {average_response_2}
Response 3: {average_response_3}

Most Frequently Best Performing Response:
------------------------------------
{most_common_best}

Operational Insights:
------------------------------------
- Evaluation workflow functioning correctly.
- Response comparison records available.
- Prompt evaluation tracking active.
- AI-assisted review pipeline operational.

Generated By:
AI Response Evaluation & Prompt Optimization Studio
"""

    return report
